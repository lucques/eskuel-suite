-- Erstelle die Tabelle "fahrlährer"
CREATE TABLE IF NOT EXISTS fahrlährer (
    kuerzel VARCHAR(10) PRIMARY KEY,
    vorname VARCHAR(50),
    nachname VARCHAR(50),
    telefonnr VARCHAR(15)
);

-- Erstelle die Tabelle "fahrschueler"
CREATE TABLE IF NOT EXISTS fahrschueler (
    nr INTEGER PRIMARY KEY AUTOINCREMENT,
    vorname VARCHAR(50),
    nachname VARCHAR(50),
    gebdatum DATE,
    theorie_bestanden BOOLEAN,
    anz_fahrstunden INT,
    fl_kuerzel VARCHAR(10),
    FOREIGN KEY (fl_kuerzel) REFERENCES fahrlährer(kuerzel)
);

